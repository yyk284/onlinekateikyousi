<h2>当テンプレートの使い方</h2>

<h3>初心者向けマニュアル公開中</h3>
<p>画像加工やテンプレートの編集方法、無料サーバーを使ってサイトを公開するなど動画をまじえてわかりやすく解説しています。<br>
<a href="/https://template-party.com/tips/index_beginner/" target="_blank">初心者向けマニュアルはこちら。</a></p>

<h3><span style="color:red">注意</span>：当テンプレートにはメインメニューが「２箇所」入っています</h3>
<p>パソコンなどの大きな端末「menubar（幅801px以上）」向けと、スマホなどの小さな端末「menubar-s（幅800px以下）」向けがそれぞれ入っています。大きな端末向けは編集ソフトで見れると思いますが、小さな端末向けは見えないと思いますのでhtml側で編集して下さい。</p>

<h3><span style="color:red">注意</span>：注意：ヘッダーの文字サイズ調整機能を使う場合の設定</h3>
<p><strong class="color1">※テンプレートのダウンロードファイルだけでは文字サイズの変更ができません。</strong><br>
海外で公開されている「styleswitcher.js」というファイルをテンプレートのjsフォルダにもってくる必要があります。<br>
<a href="/http://template-party.com/file/styleswitcher/" target="_blank">「styleswitcher.js」を配布元サイトよりDLする方法。</a></p>
<p><strong class="color1">■文字サイズボタンはIE9以降しか出ません</strong><br>
IE8以下では「文字サイズ」のタイトルのみが出て、大小のボタンは出ません。</p>
<p><strong class="color1">■文字サイズ変更を使わない場合。</strong><br>
html上部の、<br>
&lt;link rel=&quot;alternate stylesheet&quot; href=&quot;css/change.css&quot; title=&quot;change&quot;&gt;<br>
&lt;script src=&quot;js/styleswitcher.js&quot;&gt;&lt;/script&gt;<br>
の２行を削除。後はヘッダーブロックの文字サイズ変更ブロックを削除。<br>
続いてhtml側から文字サイズ変更のブロックを削除して下さい。</p>

<h3>titleタグ、copyright、metaタグ、他の設定</h3>
<p><strong class="color1">titleタグの設定はとても重要です。念入りにワードを選んで適切に入力しましょう。</strong><br>
まず、htmlソースが見れる状態にして、<br>
<span class="look">&lt;title&gt;老人ホーム・介護施設・デイサービス向け 無料ホームページテンプレート tp_home4&lt;/title&gt;</span><br>
を編集しましょう。<br>
あなたのホームページ名が「サンプルホーム」だとすれば、<br>
<span class="look">&lt;title&gt;サンプルホーム&lt;/title&gt;</span><br>
とすればＯＫです。SEO対策もするなら冒頭に重要なワードを入れておきましょう。</p>
<p><strong class="color1">copyrightを変更しましょう。</strong><br>
続いてhtmlの下の方にある、<br>
<span class="look">Copyright&copy; サンプルホーム All Rights Reserved.</span><br>
の部分もあなたのサイト名に変更します。</p>
<p><strong class="color1">metaタグを変更しましょう。</strong><br>
htmlソースが見える状態にしてmetaタグを変更しましょう。</p>
<p>ソースの上の方に、<br>
<span class="look">content=&quot;ここにサイト説明を入れます&quot;</span><br>
という部分がありますので、テキストをサイトの説明文に入れ替えます。検索結果の文面に使われる場合もありますので、見た人が来訪したくなるような説明文を簡潔に書きましょう。</p>
<p>続いて、その下の行の<br>
<span class="look">content=&quot;キーワード１,キーワード２,～～～&quot;</span><br>
も設定します。ここはサイトに関係のあるキーワードを入れる箇所です。10個前後ぐらいあれば充分です。キーワード間はカンマ「,」で区切ります。</p>
<p><strong class="color1">h1ロゴのaltタグも変更しましょう。</strong><br>
  html側に、<br>
<span class="look">alt=&quot;サンプルホーム&quot;</span><br>
となっている箇所があるので、この部分もあなたのサイト名に変更しましょう。</p>

<h3>上部のロゴ画像について</h3>
<p>文字なしの土台画像がbaseフォルダに入っていますのでそれにサイト名をのせてimagesフォルダに上書きして下さい。画像の大きさは自由に変更してもらっても構いませんがある程度大きくしておいた方が高解像度の端末で鮮明に見えます。</p>
<p><strong class="color1">ロゴサイズ変更は</strong><br>
cssフォルダのstyle.cssの「header #logo img」のwidthの数字で変更できます。各端末サイズごとに設定がある場合があるので注意して下さい。</p>

<h3>ロゴマークについて</h3>
<p>「サンプル」の「サ」から色々変換して現在のロゴマークになっています。深いデザイン性は全くありません。制作時にGoogleで類似画像を検索しましたが特にないようなのでそのまま梱包します。使ってもらっても構いませんが、利用時に他の会社が類似画像に商標権など登録していないか確認して下さい。また、当ロゴに権利を設定する事は禁止します。</p>

<h3>現在ページ表示中のメニューについて</h3>
<p>上部のメニューをクリックして開いている場合、そのメニューに色がつきますが、その説明です。</p>
<p>通常は、<br>
&lt;li&gt;&lt;a href=&quot;&quot;&gt;メニュー名&lt;/a&gt;&lt;/li&gt;<br>
のようになっていますがこれを、<br>
&lt;li <span class="color1">class=&quot;current&quot;</span>&gt;・・・<br>
とする事で装飾が入ります。自動で入るわけではないのでご注意下さい。</p>

<h3>トップページのスライドショーについて</h3>
<p><strong class="color1">各画像の下部には、必ずグラデーションを入れて下さい。</strong><br>
baseフォルダに入っている「123gradation.png」を画像の下部に重ねて下さい。または変わりの画像をご用意頂いても構いません。<br>
当テンプレートでは、スライド画像が少しだけ拡大されながら切り替わっていきます。下部にグラデーションが入っていない場合、サイズの違う部分がそのまま出てしまいます。<br>
よくわからない場合は、グラデーションなしの画像を準備して実際に置き換えてみると分かります。下にほんの少しですが他の画像が表示されてしまいます。</p>
<p><strong class="color1">新しいブラウザ(IE10以上)で動作します。</strong><br>
IE9以下では動かず最後の１枚だけが固定表示されるので、当テンプレートではスライドショーの0.jpg（※<span class="color1">class=&quot;slide0&quot;</span>の指定が入っている画像）を表示させるようトップページのhead内の条件分岐で設定しています。</p>
<p><strong class="color1">画像を入れ替えたい場合</strong><br>
「0.jpg」「1.jpg」「2.jpg」「3.jpg」の３枚のjpg画像を用意してimagesフォルダに上書きして下さい。大きさはバラバラでも構いませんが、必ず「縦横比」を合わせて下さい。<br>
縦横比を変更する場合は、cssフォルダのstyle.cssのスライドショー表示領域の設定を再調整して下さい。端末ごとに複数箇所あるので注意しましょう。<br>
画像の拡張子が「jpeg」や「JPG」と少し違った場合にうまく表示できない可能性があるので「jpg」で統一して下さい。画像を「jpg」にできない場合はhtml側の拡張子指定を変更しても構いません。</p>
<p><strong class="color1">ループを一回で終了したい場合</strong><br>
cssフォルダのslide.cssの、<br>
@keyframes slide3 {<br>
の、	<br>
90% {opacity: 0; transform: scale(1.05);}<br>
100% {opacity: 0;}<br>

を、以下に変更。<br>
90% {opacity: 1; transform: scale(1.05);}<br>
100% {opacity: 1;transform: scale(1.05);}<br>
<br>
続いて、<br>
.slide1,.slide2,.slide3 {<br>
の、<br>
animation-iteration-count:infinite;<br>
を以下に変更。<br>
animation-iteration-count:1;</p>
<p><strong class="color1">固定画像にしたい場合</strong><br>
index.htmlのhtmlの上の方にある、<br>
&lt;link rel=&quot;stylesheet&quot; href=&quot;css/slide.css&quot;&gt;<br>
の１行を削除。<br>
画像がすべて表示されるので、使わない画像を削除すればOKです。画像を囲っているasideタグなどはレイアウト設定が入っているので削除しないよう注意して下さい。</p>
<p><strong class="color1">速度や枚数などの調整</strong><br>
cssフォルダのslide.cssで行って下さい。解説も入っています。<br>
<a href="/http://template-party.com/tips/tips20160408_css_slide1/">スライドショーに関する詳しい使い方はこちら。</a></p>

<h3>１カラム、２カラムどちらでも使えます</h3>
<p>１カラムのhtmlのcontents内に、<br>
<span class="color1">&lt;div class=&quot;main&quot;&gt;</span>メインブロック<span class="color1">&lt;/div&gt;</span><br>
<span class="color1">&lt;div class=&quot;sub&quot;&gt;</span>サブブロック<span class="color1">&lt;/div&gt;</span><br>
を追加すれば、２カラムになります。</p>
<p>mainとsubの左右を入れ替えたい場合は、cssフォルダのstyle.cssのmainとsubのfloatの情報を入れ替えて下さい。</p>

<h3>トップページ以外の上部メインメニュー背景の画像について</h3>
<p>直接html側に画像を置いていますので、ページごとに好きな画像に変更する事も可能です。</p>

<h3>スクロール中に出る「↑」アイコンについて</h3>
<p>fixmenu_pagetop.jsで動作の制御を、style.cssの、<br>
<span class="color1">/*ページの上部へボタン「↑」設定</span><br>
でボタンデザインを設定しています。<br>
ボタンの出現ポイントは、現在350pxの場所になっています。変更したい場合はfixmenu_pagetop.jsの34行目あたりにある、<br>
offsettop = 350;<br>
の350を変更して下さい。</p>

<h3>プレビューでチェックすると警告メッセージが出る場合(一部ブラウザ対象)</h3>
<p>主にjavascript（jsファイル）ファイルによって出る警告ですが、WEB上では出ません。また、この警告が出ている間は効果を見る事ができないので、警告メッセージ内でクリックして解除してあげて下さい。これにより効果がちゃんと見れるようになります。</p>

